import UIKit

extension UIColor {
  var canvasBlue: UIColor {
    let color = #colorLiteral(red: 0.07058823529, green: 0.5333333333, blue: 0.8666666667, alpha: 1)
    return color
  }
  
  var canvasAqua: UIColor {
    let color = #colorLiteral(red: 0.09803921569, green: 0.5607843137, blue: 0.7176470588, alpha: 1)
    return color
  }
  
  var canvasBlack: UIColor {
    let color = #colorLiteral(red: 0.1058823529, green: 0.1529411765, blue: 0.2039215686, alpha: 1)
    return color
  }
  
  var canvasDarkGray: UIColor {
    let color = #colorLiteral(red: 0.1019607843, green: 0.1529411765, blue: 0.231372549, alpha: 1)
    return color
  }
}
